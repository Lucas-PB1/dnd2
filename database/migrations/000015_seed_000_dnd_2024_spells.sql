WITH spell_details AS (
    SELECT
        sp.id,
        sd.save_attribute,
        ad.attack_type,
        ced.character_effect_scope,
        ced.character_effect_category,
        ced.character_effect_summary
    FROM spells sp
    LEFT JOIN _import_spell_save_data sd ON sd.name = sp.name
    LEFT JOIN _import_spell_attack_data ad ON ad.name = sp.name
    LEFT JOIN _import_spell_character_effect_data ced ON ced.name = sp.name
)
UPDATE spells sp
SET
    save_attribute = sd.save_attribute,
    attack_type = sd.attack_type,
    can_affect_character = sd.character_effect_scope IS NOT NULL,
    character_effect_scope = sd.character_effect_scope,
    character_effect_category = sd.character_effect_category,
    character_effect_summary = sd.character_effect_summary,
    description = concat_ws(
        ' ',
        'D&D 2024 spell catalog entry.',
        CASE
            WHEN sp.level = 0 THEN sp.school || ' cantrip.'
            ELSE 'Level ' || sp.level || ' ' || sp.school || ' spell.'
        END,
        'Casting: ' || sp.casting_time || '.',
        'Range: ' || sp.range_text || '.',
        'Components: ' || sp.components || '.',
        CASE
            WHEN sp.material_component IS NOT NULL
                THEN 'Material: ' || sp.material_component
        END,
        'Duration: ' || sp.duration_text || '.',
        CASE
            WHEN sd.save_attribute IS NOT NULL
                THEN 'Primary save: ' || sd.save_attribute || '.'
        END,
        CASE
            WHEN sd.attack_type IS NOT NULL
                THEN 'Attack type: ' || sd.attack_type || '.'
        END,
        CASE
            WHEN sd.character_effect_summary IS NOT NULL
                THEN 'Character effect: '
                    || sd.character_effect_category
                    || ' / '
                    || sd.character_effect_scope
                    || '. '
                    || sd.character_effect_summary
        END
    )
FROM spell_details sd
WHERE sd.id = sp.id;


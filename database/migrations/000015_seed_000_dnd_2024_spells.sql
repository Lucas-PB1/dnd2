INSERT INTO spell_rolls (
    spell_id,
    roll_label,
    roll_type,
    damage_type_id,
    damage_type_options,
    formula,
    base_cast_level,
    minimum_cast_level,
    scaling_type,
    scaling_formula,
    roll_context,
    sort_order
)
SELECT
    sp.id,
    rd.roll_label,
    rd.roll_type,
    dt.id,
    rd.damage_type_options,
    rd.formula,
    rd.base_cast_level,
    rd.minimum_cast_level,
    rd.scaling_type,
    rd.scaling_formula,
    rd.roll_context,
    rd.sort_order
FROM _import_spell_roll_data rd
JOIN spells sp ON sp.name = rd.name
LEFT JOIN damage_types dt ON dt.name = rd.damage_type;


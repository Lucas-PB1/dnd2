CREATE TABLE weapon_masteries (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL UNIQUE,
    description TEXT,
    effect_id INT REFERENCES effects(id) ON DELETE SET NULL
);

CREATE TABLE weapon_properties (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL UNIQUE,
    description TEXT
);

CREATE TABLE weapons (
    item_id INT PRIMARY KEY REFERENCES items(id) ON DELETE CASCADE,
    damage_effect_id INT REFERENCES effect_damage(effect_id) ON DELETE SET NULL,
    mastery_id INT REFERENCES weapon_masteries(id) ON DELETE SET NULL,
    weapon_category VARCHAR(20) NOT NULL CHECK (
        weapon_category IN ('Simple Melee', 'Simple Ranged', 'Martial Melee', 'Martial Ranged')
    ),
    range_normal INT CHECK (range_normal IS NULL OR range_normal >= 0),
    range_long INT CHECK (range_long IS NULL OR range_long >= 0),
    is_two_handed BOOLEAN NOT NULL DEFAULT FALSE,
    CHECK (
        range_normal IS NULL
        OR range_long IS NULL
        OR range_long >= range_normal
    )
);

CREATE TABLE weapon_has_properties (
    item_id INT REFERENCES weapons(item_id) ON DELETE CASCADE,
    property_id INT REFERENCES weapon_properties(id) ON DELETE CASCADE,
    details VARCHAR(100),
    PRIMARY KEY (item_id, property_id)
);
